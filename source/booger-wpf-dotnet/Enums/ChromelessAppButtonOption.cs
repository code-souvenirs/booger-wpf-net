namespace Jsinh.BoogerWpf
{
    /// <summary>
    /// Defines list of chromeless windows application button options.
    /// </summary>
    public enum ChromelessAppButtonOption
    {
        /// <summary>
        /// Show minimize and exit application buttons.
        /// </summary>
        MinimizeAndExit,

        /// <summary>
        /// Show exit application button only.
        /// </summary>
        ExitOnly,

        /// <summary>
        /// Show all application buttons - Minimize, Maximize / Restore, Exit
        /// </summary>
        ShowAll
    }
}
